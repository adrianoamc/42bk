int	ft_strcmp(char *s1, char *s2)
{
	int i_c;

	i_c = 0;
	while (s1[i_c] != '\0' && s2[i_c] != '\0' && s1[i_c] == s2[i_c])
		i_c++;
	return (s1[i_c] - s2[i_c]);
}
